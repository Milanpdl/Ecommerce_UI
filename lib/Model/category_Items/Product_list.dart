class Product {
  String name;
  String brand;
  double price;
  String category;
  int reviews;
  double rating;
  String description;
  String image;

  Product({
    required this.name,
    required this.brand,
    required this.category,
    required this.rating,
    required this.reviews,
    required this.description,
    required this.image,
    required this.price,
  });
}
